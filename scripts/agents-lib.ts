import { fileURLToPath } from 'url';
import yaml from 'yaml';

const __dirname = path.dirname(fileURLToPath(import.meta.url));
export const REPO_ROOT = path.resolve(__dirname, '..');

export type StepStatus = 'active' | 'inactive' | 'deprecated';
export type StepKind = 'edge_function' | 'worker' | 'rpc' | 'manual' | 'maintenance_script';

export interface CronConfig {
  job_name: string;
  schedule: string;
  sql: string;
  body?: string;
}

export interface ManifestStep {
  id: string;
  deploy_name?: string;
  division: string;
  workflow: string;
  kind: StepKind;
  status: StepStatus;
  source?: string;
  cron?: CronConfig;
  invokes?: string[];
  invoked_by?: string[];
  secrets?: string[];
  tables?: string[];
  verify_jwt?: boolean;
  description?: string;
}

export interface Manifest {
  version: number;
  vault_secrets: string[];
  workers?: Record<string, { path: string; invokes?: string[] }>;
  steps: ManifestStep[];
}

export function loadManifest(): Manifest {
  const raw = fs.readFileSync(path.join(REPO_ROOT, 'doxa-agents', 'manifest.yaml'), 'utf8');
  return yaml.parse(raw) as Manifest;
}

export function manifestPath(...parts: string[]): string {
  return path.join(REPO_ROOT, 'doxa-agents', ...parts);
}

export function supabaseFunctionsDir(): string {
  return path.join(REPO_ROOT, 'supabase', 'functions');
}

export function handlerPath(step: ManifestStep): string | null {
  if (!step.source) return null;
  return path.join(REPO_ROOT, step.source, 'handler.ts');
}

export function stubPath(deployName: string): string {
  return path.join(supabaseFunctionsDir(), deployName, 'index.ts');
}

export function relativeStubImport(deployName: string, step: ManifestStep): string {
  const stubDir = path.join(supabaseFunctionsDir(), deployName);
  const handler = handlerPath(step)!;
  let rel = path.relative(stubDir, handler).replace(/\\/g, '/');
  if (!rel.startsWith('.')) rel = `./${rel}`;
  return rel;
}

export function parseCronSql(sqlContent: string): Array<{ job_name: string; schedule: string; deploy_name?: string }> {
  const jobs: Array<{ job_name: string; schedule: string; deploy_name?: string }> = [];
  const scheduleRe = /cron\.schedule\(\s*'([^']+)'\s*,\s*'([^']+)'/g;
  let m: RegExpExecArray | null;
  while ((m = scheduleRe.exec(sqlContent)) !== null) {
    const job_name = m[1];
    const schedule = m[2];
    const slice = sqlContent.slice(m.index, m.index + 800);
    const fnMatch = slice.match(/\/functions\/v1\/([a-zA-Z0-9_-]+)/);
    jobs.push({ job_name, schedule, deploy_name: fnMatch?.[1] });
  }
  return jobs;
}

export function extractInvokedDeployNames(source: string): string[] {
  const names = new Set<string>();
  const urlRe = /\/functions\/v1\/([a-zA-Z0-9_-]+)/g;
  let m: RegExpExecArray | null;
  while ((m = urlRe.exec(source)) !== null) names.add(m[1]);
  const invokeRe = /invokeFunction\([^,]+,[^,]+,\s*["']([a-zA-Z0-9_-]+)["']/g;
  while ((m = invokeRe.exec(source)) !== null) names.add(m[1]);
  return [...names];
}

