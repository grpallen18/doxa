export function extractSecrets(source: string): string[] {
  const skip = new Set(['SUPABASE_URL', 'SUPABASE_SERVICE_ROLE_KEY']);
  const secrets = new Set<string>();
  const re = /Deno\.env\.get\(\s*["']([A-Z0-9_]+)["']\s*\)/g;
  let m: RegExpExecArray | null;
  while ((m = re.exec(source)) !== null) {
    if (!skip.has(m[1])) secrets.add(m[1]);
  }
  return [...secrets].sort();
}

export function parseCronBody(sqlContent: string, jobName: string): string | undefined {
  const re = new RegExp(
    `cron\\.schedule\\(\\s*'${jobName.replace(/[.*+?^${}()|[\]\\]/g, '\\$&')}'\\s*,\\s*'[^']+'\\s*,\\s*\\$\\$([\\s\\S]*?)\\$\\$`,
  );
  const m = re.exec(sqlContent);
  if (!m) return undefined;
  const block = m[1];
  const bodyMatch = block.match(/body\s*:=\s*'([^']+)'::jsonb/);
  return bodyMatch?.[1];
}

export function isMaintenanceOnlySql(sqlContent: string): boolean {
  return sqlContent.includes('cron.unschedule') && !sqlContent.includes('cron.schedule');
}

export function parseVerifyJwtDisabled(configContent: string): Set<string> {
  const disabled = new Set<string>();
  const re = /\[functions\.([^\]]+)\]\s*\nverify_jwt\s*=\s*false/g;
  let m: RegExpExecArray | null;
  while ((m = re.exec(configContent)) !== null) disabled.add(m[1]);
  return disabled;
}

export function scanAppApiInvokes(): Map<string, string[]> {
  const map = new Map<string, string[]>();
  const apiDir = path.join(REPO_ROOT, 'app', 'api');
  if (!fs.existsSync(apiDir)) return map;

  function walk(dir: string) {
    for (const ent of fs.readdirSync(dir, { withFileTypes: true })) {
      const full = path.join(dir, ent.name);
      if (ent.isDirectory()) walk(full);
      else if (ent.name.endsWith('.ts')) {
        const content = fs.readFileSync(full, 'utf8');
        for (const deploy of extractInvokedDeployNames(content)) {
          const list = map.get(deploy) ?? [];
          if (!list.includes('app')) list.push('app');
          map.set(deploy, list);
        }
      }
    }
  }
  walk(apiDir);
  return map;
}

